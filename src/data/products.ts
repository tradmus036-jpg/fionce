export const activeProducts = products
  .filter((p) => !p.isArchived)
  .sort((a, b) => {
    if (a.id === "2") return -1;
    if (b.id === "2") return 1;
    return (b.isBundle ? 1 : 0) - (a.isBundle ? 1 : 0);
  });

export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.isFeatured && !p.isArchived);
}

export function getRelatedProducts(currentSlug: string, category: string, limit = 4): Product[] {
  return products
    .filter((p) => p.slug !== currentSlug && p.category === category)
    .slice(0, limit)
    .concat(
      products
        .filter((p) => p.slug !== currentSlug && p.category !== category)
        .slice(0, Math.max(0, limit - products.filter((p) => p.slug !== currentSlug && p.category === category).length))
    )
    .slice(0, limit);
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter((p) => p.category === category);
}

